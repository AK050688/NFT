// Helper functions
export const getNFTById = (id) => {
  return nftData.find(nft => nft.id === id);
};

export const getNFTsByCollection = (collection) => {
  return nftData.filter(nft => nft.collection === collection);
};

export const getNFTsByCreator = (creator) => {
  return nftData.filter(nft => nft.creator === creator);
};

export const filterNFTs = (filters = {}) => {
  let filtered = [...nftData];
  
  if (filters.category && filters.category !== 'All') {
    filtered = filtered.filter(nft => nft.collection.toLowerCase().includes(filters.category.toLowerCase()));
  }
  
  if (filters.priceRange) {
    filtered = filtered.filter(nft => {
      const price = parseFloat(nft.price);
      return price >= filters.priceRange.min && price <= filters.priceRange.max;
    });
  }
  
  if (filters.status) {
    filtered = filtered.filter(nft => nft.status === filters.status);
  }
  
  if (filters.rarity) {
    filtered = filtered.filter(nft => nft.rarity === filters.rarity);
  }
  
  return filtered;
}; 